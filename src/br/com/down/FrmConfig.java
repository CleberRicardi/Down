    public File fileChooser(String path, boolean file){
        JFileChooser jfc = new JFileChooser(path);
        if(!file){ jfc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY); }
        jfc.showOpenDialog(this);
        return jfc.getSelectedFile();
    }
    
    
    
