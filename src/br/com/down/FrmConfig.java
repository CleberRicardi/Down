    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try { 
          FileConfig fc;
          fc = new FileConfig();

          fc.setDownloadPath(txtDownloadPath.getText());
          fc.setProxyHost(txtProxyHost.getText());
          fc.setProxyPort(txtProxyPort.getText());
          fc.setProxyPass(txtProxyPass.getText());
          fc.setProxyUser(txtProxyUser.getText());
          fc.setUseProxy(checkProxy.isSelected());
          if (fc.save()){
              JOptionPane.showMessageDialog(null,"Configurações Salvas!");
              this.setVisible(false);
          }else{
              JOptionPane.showMessageDialog(null,"Falha ao salvar Configurações!");
          }
        } catch (IOException ex) {
          Logger.getLogger(FrmConfig.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            FileConfig fc = new FileConfig();
            txtDownloadPath.setText(fc.getDownloadPath());
            txtProxyHost.setText(fc.getProxyHost());
            txtProxyPort.setText(fc.getProxyPort());
            txtProxyUser.setText(fc.getProxyUser());
            txtProxyPass.setText(fc.getProxyPass());
            checkProxy.setSelected(fc.isUseProxy());
            setCamposProxy(checkProxy.isSelected());            
        } catch (IOException ex) {
            Logger.getLogger(FrmConfig.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_formWindowOpened

